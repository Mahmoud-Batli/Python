myString = "I Love Python"
print(myString[0])      # First character
print(myString[7])      # Eighth character
print(myString[-1])     # Last character
print(myString[-7])     # Seventh from last character
print(myString[2:6])    # Characters from index 2 to 5
print(myString[:6])     # First five characters 
print(myString[7:])     # Characters from index 7 to end
print(myString[-6:])    # Last six characters
print(myString[:-7])    # All but last six characters
print(myString[:])      # The whole string
print(myString[::2])    # Every second character
print(myString[1::2])   # Every second character starting from index 1
print(myString[::-1])   # The whole string in reverse

















